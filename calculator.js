exports.sum = function (number1, number2) {
  return number1 + number2;
}
exports.multiply = function (number1, number2) {
  return number1 * number2;
}
exports.reduce = function (number1, number2) {
  return number1 - number2;
}
exports.divide = function (number1, number2) {
  return number1 / number2;
}
